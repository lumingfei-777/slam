"""
配置文件 —— 所有可调参数集中管理

为什么要集中管理？
  1) 方便调参，不用翻遍所有源代码
  2) 告诉学习者每个参数的含义和作用范围

坐标系约定：
  x 向右，y 向下，z 向前（相机朝向）
  平移单位：毫米 (mm)
"""

import os
import numpy as np

# ═══════════════════════════════════════════
#  路径配置
# ═══════════════════════════════════════════

BASE_DIR = os.path.dirname(os.path.abspath(__file__))
OUT_DIR  = os.path.join(BASE_DIR, 'output')  # 所有输出（csv、图）放这里

# ═══════════════════════════════════════════
#  图像尺寸
# ═══════════════════════════════════════════

WIDTH, HEIGHT = 848, 480  # 红外图分辨率

# ═══════════════════════════════════════════
#  左红外相机内参矩阵 K
#
#  K = [fx  0  cx]
#      [ 0  fy  cy]
#      [ 0   0   1]
#
#  fx, fy: 焦距（像素单位）
#  cx, cy: 光心（主点）位置
#  作用：将 3D 点投影到 2D 图像平面
#  用户公式：u = fx * X/Z + cx,  v = fy * Y/Z + cy
# ═══════════════════════════════════════════

K_LEFT = np.array([
    [596.25,   0.,    424.],
    [  0.,   540.,    240.],
    [  0.,     0.,      1.]
], dtype=np.float64)

# 右目直接用左目内参（教学简化，真实系统应各自标定）
K_RIGHT = K_LEFT.copy()

# 畸变系数（红外相机）[k1, k2, p1, p2, k3]
# 用于 PnP 求解时对特征点去畸变
D_IR = np.array([0.1, -0.2, 0.001, 0.001, 0.05], dtype=np.float64)

# ═══════════════════════════════════════════
#  双目外参（右目相对于左目）
#
#  双目 SLAM 的核心：已知两个相机之间的固定位置关系，
#  可以从左右图的视差恢复深度。
#
#  R_STEREO:  右目到左目的旋转矩阵
#  BASELINE_MM: 基线距离（两个相机光心之间的间距）
#  T_STEREO:  右目到左目的平移向量
#
#  深度计算公式：Z = fx * baseline / disparity
#  基线越长，深度精度越高，但立体匹配越难
# ═══════════════════════════════════════════

R_STEREO = np.eye(3, dtype=np.float64)   # 假设左右目平行（无相对旋转）
BASELINE_MM = 95.0                        # 95mm 基线
T_STEREO = np.array([-BASELINE_MM, 0.0, 0.0], dtype=np.float64)

# ═══════════════════════════════════════════
#  ORB 特征提取参数
#
#  ORB = Oriented FAST + Rotated BRIEF
#  既能检测角点，又能生成旋转不变的描述子
#  是视觉 SLAM 中最常用的特征之一
# ═══════════════════════════════════════════

ORB_N_FEATURES   = 800    # 每帧最多提取的特征点数
ORB_SCALE_FACTOR = 1.2    # 图像金字塔缩放比（每层缩小 1.2 倍）
ORB_N_LEVELS     = 4      # 金字塔层数（层数越多对尺度变化越鲁棒）

# ═══════════════════════════════════════════
#  特征匹配
# ═══════════════════════════════════════════

# Lowe's ratio test 阈值
# 对于每个特征点，找最近邻和次近邻
# 如果最近邻距离 << 次近邻距离，才认为是可靠匹配
# 0.75 是 SIFT 论文推荐的经典值
MATCH_RATIO_TEST = 0.75

# ═══════════════════════════════════════════
#  PnP 位姿估计
#
#  PnP = Perspective-n-Point
#  已知 3D 地图点和它们在当前帧的 2D 投影，
#  反推相机在当前帧的位姿（旋转 + 平移）
# ═══════════════════════════════════════════

PNP_MIN_MATCHES    = 10     # 最少需要多少对 2D-3D 匹配
PNP_ITERATIONS     = 200    # RANSAC 迭代次数
PNP_REPROJ_ERROR   = 8.0    # 重投影误差阈值（像素），超过视为外点
PNP_CONFIDENCE     = 0.99   # 期望的置信度（越高迭代越久）

# ═══════════════════════════════════════════
#  关键帧选择
#
#  关键帧 = 被选中加入地图的帧
#  不是每一帧都要加入地图，只有"信息量大"的帧才值得保存
#  判断标准：相机移动足够大 / 旋转足够大 / 追踪到的特征太少
# ═══════════════════════════════════════════

KF_MIN_TRANSLATION  = 200.0    # mm — 平移超过 20cm 就选为关键帧
KF_MIN_ROTATION     = 0.15     # rad — 旋转超过 ~8.6° 就选为关键帧
KF_MIN_TRACKED_RATIO = 0.4     # 与上一个关键帧的共同特征少于 40% 就选为关键帧

# ═══════════════════════════════════════════
#  局部 BA（Bundle Adjustment）
#
#  BA 是最小化重投影误差的优化问题
#  同时调整相机位姿和地图点位置
#  局部 BA 只优化最近几个关键帧，保证实时性
# ═══════════════════════════════════════════

BA_WINDOW  = 3   # 优化最近 N 个关键帧
BA_MAX_ITER = 10 # 最大迭代次数（教学简化版不需要收敛到很高精度）

# ═══════════════════════════════════════════
#  回环检测
#
#  回环：相机回到了之前去过的地方
#  关键：检测到回环后，可以消除长时间运行的累积漂移
# ═══════════════════════════════════════════

LC_MIN_MATCHES = 15  # 两个关键帧至少有 N 个共同地图点，才认为"对齐可靠"
LC_SEARCH_DIST = 5   # 跳过最近 N 帧（避免把自己检测为回环）
